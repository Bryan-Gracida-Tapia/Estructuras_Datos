# Creamos todas nuestras variables y les asignamos el tipo de dato requerido.
dia = 'Lunes'       # Usamos una variable de tipo String
pasaje = 20     # Usamos una variable de tipo Int
comida = 135.5      # Usamos una variable de tipo Float
otros_gastos = 34.5     # Usamos una variable de tipo Float
variable_presupuesto = True    # Usamos una variable de tipo Booleana

# Se imprimen todas las variables con información adicional para mejor comprensión
print(" ***  Gastos Diarios  ***")
print("Día:", dia)
print("Pasaje:", pasaje)
print("Comida: ", comida, "$.")
print("Otros gastos: ", otros_gastos, "$.")
print("Fue mayor a mi presupuesto?: ", variable_presupuesto)

# Se modifican las variables.
